     public ArrayList<Subject> subjectList(){
        ArrayList<Subject> subjectList = new ArrayList<>();
         try {
            Class.forName("com.mysql.jdbc.Driver");
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/studentmanagement","root","");
            String query1 = "select * from addsubject";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query1);
            Subject subject;
            while(rs.next()){
                subject=new Subject(rs.getString("SubjectCode"), rs.getString("SubjectName"));
                subjectList.add(subject);
            }
            
    }   catch (ClassNotFoundException ex) {
            Logger.getLogger(StudentDetails.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(StudentDetails.class.getName()).log(Level.SEVERE, null, ex);
        }
         catch(Exception e){
             JOptionPane.showMessageDialog(null, e);
         }
         return subjectList;
    }
    
